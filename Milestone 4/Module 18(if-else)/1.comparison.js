/* 
Comparison 
1. bigger: > 
2. less: < 
3. equal: == 
4. not equal: != 
5. less that equal: <= 
6. greater than equal: >= 
8. and: &&
9. or: || 
10. not: !
*/


console.log(10 > 12); // false 
console.log(10 < 12); // true 
console.log(20 == 20); // true 
console.log(10 <= 20); //true 
console.log(10 >= 12); // false 
console.log(true && false); // false
console.log(true || false); // true 
console.log(!false); //true 